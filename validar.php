<?php

class matematica {

    public static function sumar($n1,$n2){
        $suma=$n1+$n2;
        return $suma;
    }

    public static function restar($n1,$n2){
        $resta=$n1-$n2;
        return $resta;
    }

    public static function multiplicar($n1,$n2){
        $multiplicar=$n1*$n2;
        return $multiplicar;
    }

    public static function dividir($n1,$n2){
        $dividir=$n1/$n2;
        return $dividir;
    }
}
?>